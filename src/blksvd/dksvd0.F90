       USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL64
       IMPLICIT NONE
       REAL(KIND=REAL64), INTENT(IN) :: G(2,2)
       REAL(KIND=REAL64), INTENT(OUT) :: U(2,2), V(2,2), S(2)
       INTEGER, INTENT(INOUT) :: INFO
     END SUBROUTINE DKSVD2
  END INTERFACE
  INTERFACE
     PURE SUBROUTINE DCVGPP(G, U, V, S, INFO)
       USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL64
       IMPLICIT NONE
       REAL(KIND=REAL64), INTENT(IN) :: G(2,2), U(2,2), V(2,2)
       REAL(KIND=REAL64), INTENT(INOUT) :: S(2)
       INTEGER, INTENT(INOUT) :: INFO
     END SUBROUTINE DCVGPP
  END INTERFACE
  INTERFACE
     PURE SUBROUTINE DROTC(M, N, G, LDG, P, Q, W, INFO)
       USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL64
       IMPLICIT NONE
       INTEGER, INTENT(IN) :: M, N, LDG, P, Q
       REAL(KIND=REAL64), INTENT(INOUT) :: G(LDG,N)
       REAL(KIND=REAL64), INTENT(IN) :: W(2,2)
       INTEGER, INTENT(INOUT) :: INFO
     END SUBROUTINE DROTC
  END INTERFACE
  INTERFACE
     PURE SUBROUTINE DROTR(M, N, G, LDG, P, Q, W, INFO)
       USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL64
       IMPLICIT NONE
       INTEGER, INTENT(IN) :: M, N, LDG, P, Q
       REAL(KIND=REAL64), INTENT(INOUT) :: G(LDG,N)
       REAL(KIND=REAL64), INTENT(IN) :: W(2,2)
       INTEGER, INTENT(INOUT) :: INFO
     END SUBROUTINE DROTR
  END INTERFACE
  INTERFACE
     PURE SUBROUTINE JSTEP(J, N, S, T, P, O, R, INFO)
       IMPLICIT NONE
       INTEGER, INTENT(IN) :: J, N, S, T, P, O(*)
       INTEGER, INTENT(OUT) :: R(2,P), INFO
     END SUBROUTINE JSTEP
  END INTERFACE
#ifdef ANIMATE
  INTERFACE
     FUNCTION PVN_RVIS_FRAME(ctx, A, ldA) BIND(C,name='pvn_rvis_frame_')
       USE, INTRINSIC :: ISO_C_BINDING, ONLY: c_int, c_size_t, c_ptr, c_double
       IMPLICIT NONE
       TYPE(c_ptr), INTENT(IN), TARGET :: ctx
       INTEGER(KIND=c_size_t), INTENT(IN), TARGET :: ldA
       REAL(KIND=c_double), INTENT(IN), TARGET :: A(ldA,*)
       INTEGER(KIND=c_int) :: PVN_RVIS_FRAME
     END FUNCTION PVN_RVIS_FRAME
  END INTERFACE
#endif

  INTEGER, PARAMETER :: K = REAL64, USID = 8, UACC = 16, VSID = 32, VACC = 64
  REAL(KIND=K), PARAMETER :: ZERO = 0.0_K, ONE = 1.0_K
  INTEGER, INTENT(IN) :: JOB, N, LDG, LDU, LDV
  REAL(KIND=K), INTENT(INOUT), TARGET :: G(LDG,N)
  REAL(KIND=K), INTENT(INOUT) :: U(LDU,N), V(LDV,N)
  REAL(KIND=REAL128), INTENT(INOUT), TARGET :: SV(N)
  REAL(KIND=K), INTENT(INOUT) :: W(MAX(N,3)*N)
  INTEGER, INTENT(INOUT) :: O(2*N*(N-1)), INFO

  INTEGER, POINTER, CONTIGUOUS :: R(:,:)
  REAL(KIND=K) :: G2(2,2), U2(2,2)
  REAL(KIND=K) :: GN, UN, VN
  INTEGER(KIND=INT64) :: TT, TM, SM
  INTEGER :: MRQSTP, I, J, L, M, M_2, NP, NS, P, Q, T, JS, GS, US, VS, WV, WS, STP, XSG, XSU, XSV
  LOGICAL :: LOMP, LUSID, LUACC, LVSID, LVACC
#ifdef ANIMATE
  TYPE(c_ptr), TARGET :: CTX
  TYPE(c_ptr), POINTER :: CP
  INTEGER(KIND=c_size_t), TARGET :: LDF
#endif

#define LANGO DLANGO
#define SCALG DSCALG
#define MK3PQ DMK3PQ
#define KSVD2 DKSVD2
#define CVGPP DCVGPP
#define ROTC DROTC
#define ROTR DROTR
#include "gksvd0.F90"
#ifndef NDEBUG
9 FORMAT(A,ES25.17E3)
#endif
END SUBROUTINE DKSVD0
