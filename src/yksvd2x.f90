  !>@brief \b YTEST calls KSVD2.
  !!
  !!@param KSVD2 is YKSVD2.
  SUBROUTINE YTEST(KSVD2)
    USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY: REAL128
    IMPLICIT NONE
    INTEGER, PARAMETER :: IERR = -HUGE(0), KX = REAL128, CLAL = 132
    EXTERNAL :: KSVD2
    CHARACTER(LEN=CLAL) :: CLA
    COMPLEX(KIND=REAL128) :: G(2,2), U(2,2), V(2,2)
    REAL(KIND=REAL128) :: S(2)
    INTEGER :: INFO
    COMPLEX(KIND=KX) :: GX(2,2), UX(2,2), VX(2,2)
    REAL(KIND=KX) :: SX(2,2)
    INCLUDE 'uksvd2.f90'
1   FORMAT(A,ES45.36E4)
2   FORMAT(A,'(',ES45.36E4,',',ES45.36E4,')')
3   FORMAT(A,I6)
  END SUBROUTINE YTEST
END PROGRAM YKSVD2X
