       REAL(KIND=REAL128), INTENT(IN) :: E
       INTEGER, INTENT(OUT) :: P, Q
     END SUBROUTINE QDEC
  END INTERFACE
#endif
  REAL(KIND=REAL128), PARAMETER :: WZERO = 0.0_REAL128
